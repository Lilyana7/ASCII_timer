void timer(int hours, int minutes, int seconds)
{
    while (seconds != 0 || minutes != 0 || hours != 0)
    {
        if (hours == 0 && minutes == 0 && seconds == 0)
        {
            break;
        }
        if (seconds == 0 && minutes == 0)
        {
            hours--;
            minutes = 60;
        }
        if (seconds == 0)
        {
            minutes--;
            seconds = 60;
        }
        system("cls");
        cout << hours << ":" << minutes << ":" << seconds--;
        Sleep(1000);
    }
}
